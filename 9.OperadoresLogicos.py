#Operadores utilizados em conjunto com operadores de comparação
# para montar uma expressão lógica
# Combinar operadores de comparaçãocom operadores lógicos 


#Operador and - E

saldo = 1000
saque = 200
limite = 100

res = saldo >= saque and saque <= limite
print(res)

#Operador or - OU

saldo = 1000
saque = 200
limite = 100

res = saldo >= saque or saque <= limite
print(res)

#Operador negação - not

print(not 1000 > 1500)

#Verificando se a lista está vazia
contatos_emergencia = []
not contatos_emergencia

#Verificando se a string é vazia
print(not "saque 1500")
print(not "")
