#Verificar se um objeto está presente em uma sequencia

curso = "Curso de Python"
frutas = ["laranja","uva","limao"]
saques = [1500,100]

#Verificando se a palavra Python está na string
print("Python" in curso)

#Verificando se a palavra maçã não está na lista
