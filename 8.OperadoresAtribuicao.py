#Utilizados para definir um valor inicial a uma varivel

#Atribuição simples
saldo = 500
print(saldo)

#Atribuição com soma
saldo = 500
saldo += 200
#saldo = saldo + 200
print(saldo)

#Atribuição com subtração
saldo = 500
saldo -= 200
#saldo = saldo - 200
print(saldo)

#Atribuição com multiplicação
saldo = 500
saldo *= 200
#saldo = saldo * 200
print(saldo)

#Atribuição com divisão simples
saldo = 500
saldo /= 200
#saldo = saldo / 200
print(saldo)

#Atribuição com divisão inteiro
saldo = 500
saldo //= 200
#saldo = saldo / 200
print(saldo)

#Atribuição com modulo
saldo = 500
saldo %= 200
#saldo = saldo % 200
print(saldo)

#Atribuição com exponenciação
saldo = 5
saldo **= 2
#saldo = saldo ** 200
print(saldo)
